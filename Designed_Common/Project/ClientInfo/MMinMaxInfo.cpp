#include "MMinMaxInfo.h"
#include <fstream>

//--------------------------------------------------------------------------------
// Save to File
//--------------------------------------------------------------------------------
void		
MMinMaxInfo::SaveToFile(std::ofstream& file)
{
	file.write((const char*)&Min, 4);
	file.write((const char*)&Max, 4);
}

//--------------------------------------------------------------------------------
// Load to file
//--------------------------------------------------------------------------------
void		
MMinMaxInfo::LoadFromFile(ivfstream& file)
{
	file.read((char*)&Min, 4);
	file.read((char*)&Max, 4);
}
